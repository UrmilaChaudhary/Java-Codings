public class Diwali_Loop
{

	public static void main(String[] args) 
	{
		//declaring string
         String s = "HAPPY DIWALI"; 
		
       //using for loop
		for (int i=0;i<s.length();i++)
		{
			
			System.out.println(s);
			
			//break statement
			break;
		}
	}

}
